    //     );
    // }

    /**
     * Get the attachments for the message.
     *
     * @return array
     */
    // public function attachments()
    // {
    //     return [];
    // }
    public function build()
    {
        return $this->subject("Trung Tâm Dạy Học IT ")
            ->view('sendmail.xac_nhan_doi_khoa')
            ->with('emails', $this->email);
    }
}
